// Helper function to get just the word strings (for backward compatibility)
export function getWordStrings(category: string): string[] {
  const categoryWords = words[category as keyof typeof words];
  if (!categoryWords) return [];
  return categoryWords.map(w => w.word);
}

// Helper function to get a random word with its taboo words
export function getRandomWord(category: string): WordWithTaboo | null {
  const categoryWords = words[category as keyof typeof words];
  if (!categoryWords || categoryWords.length === 0) return null;
  return categoryWords[Math.floor(Math.random() * categoryWords.length)];
}

